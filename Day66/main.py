import numpy as np


marks = np.array([85, 92, 78, 88, 95])

print("Student Marks")

print(marks)

print()

print("First Mark:")

print(marks[0])

print()

print("Highest Mark:")

print(np.max(marks))

print()

print("Lowest Mark:")

print(np.min(marks))

print()

print("Average Mark:")

print(np.mean(marks))

print()

print("Total Marks:")

print(np.sum(marks))